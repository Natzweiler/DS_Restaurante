        /**
     * Maneja el evento de acción del botón "Guardar Cambios".
     * Recoge los datos ingresados por el usuario, valida que no estén vacíos,
     * y luego actualiza el objeto {@link MeseroDTO}. Finalmente, llama a la
     * capa de negocio para persistir los cambios.
     * Muestra mensajes de éxito o error al usuario.
     *
     * @param evt El evento de acción.
     */
    private void btnGuardarCambiosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarCambiosActionPerformed
        // TODO add your handling code here:
        try {
            if (txtNombre.getText().isEmpty() || txtDireccion.getText().isEmpty() || txtTelefono.getText().isEmpty() ) {
                JOptionPane.showMessageDialog(this, "Por favor llena todos los campos.");
                return;
            }
            
        String nombre = txtNombre.getText().trim();
        String telefono = txtTelefono.getText().trim();
        String direccion = txtDireccion.getText().trim();
        
        int dia = Integer.parseInt(comboDia.getSelectedItem().toString());
        int mes = comboMes.getSelectedIndex()+1;
        int año = Integer.parseInt(comboAño.getSelectedItem().toString());
        LocalDate fechaNacimiento = LocalDate.of(año, mes, dia);
        
        mesero.setNombre(nombre);
        mesero.setTelefono(telefono);
        mesero.setDireccion(direccion);
        mesero.setFechaNacimiento(fechaNacimiento);
        
        IMeseroDAO meseroDAO = MeseroDAO.getInstanceDAO();
        IMeseroBO meseroBO = new MeseroBO(meseroDAO);
        MeseroDTO actualizado = meseroBO.actualizarMesero(mesero);
            if (actualizado != null) {
                JOptionPane.showMessageDialog(this, "Mesero actualizado correctamente.");
            }else{
            JOptionPane.showMessageDialog(this, "No se modifico el mesero.");
            }
        } catch (NegocioException e) {
            JOptionPane.showMessageDialog(this, "No se pudo actualizar el mesero.");
        }catch(DateTimeException dte){
            JOptionPane.showMessageDialog(this, "Fecha invalida."); 
        }
    }//GEN-LAST:event_btnGuardarCambiosActionPerformed

